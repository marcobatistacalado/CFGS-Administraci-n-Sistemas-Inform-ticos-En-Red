CREATE OR REPLACE PROCEDURE MUESTRO_10 (PNUM1 NUMBER, PNUM2 NUMBER)
AS
MENOR NUMBER;
MAYOR NUMBER;
CUANTOS NUMBER := 0;
TEXTO VARCHAR2(40);
BEGIN
IF PNUM1 < PNUM2 THEN 
   MENOR:=PNUM1;
   MAYOR:=PNUM2;
ELSE
   MENOR:=PNUM2;
   MAYOR:=PNUM1; 
END IF;  
FOR CONT IN MENOR..MAYOR LOOP
    IF MOD (CONT, 10) = 0 THEN 
       VER (CONT);
       TEXTO := TEXTO || CONT||',';
       CUANTOS := CUANTOS + 1;
    END IF;   
END LOOP;
TEXTO := SUBSTR (TEXTO,1,LENGTH(TEXTO)-1); 
IF CUANTOS > 0 THEN 
   VER ('HAY '||CUANTOS||' MULTIPLOS DE 10 QUE SON '||TEXTO);
ELSE
   VER ('HAY '||CUANTOS||' MULTIPLOS DE 10');
END IF;
END;
