



CREATE OR REPLACE PROCEDURE FOR_MUESTRO_1_10 
AS
BEGIN
  VER ('LOS MUESTRO DE MENOR A MAYOR');
  FOR CONT IN 1..10 LOOP
    VER (CONT);
  END LOOP;
  VER ('LOS MUESTRO DE MAYOR A MENOR');
  FOR CONT IN REVERSE 1..10 LOOP
    VER (CONT);
  END LOOP;
END;
CREATE OR REPLACE PROCEDURE VER (ALGO VARCHAR2)
AS
BEGIN
 DBMS_OUTPUT.PUT_LINE(ALGO);
END; 

EXECUTE FOR_MUESTRO_1_10;


