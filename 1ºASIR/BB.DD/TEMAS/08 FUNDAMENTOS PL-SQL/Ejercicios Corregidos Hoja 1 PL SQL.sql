CREATE OR REPLACE PROCEDURE EJERCICIO_12(PBASE NUMBER, PALTURA NUMBER)
AS
  AREA NUMBER;
BEGIN
  IF  PBASE > 0  AND PALTURA > 0  THEN
      AREA := ((PBASE * PALTURA) / 2);
      DBMS_OUTPUT.put_line('Buenos días, procedamos a calcular el área de un triángulo:');
      DBMS_OUTPUT.put_line('La base del triángulo con tus medidas será: ' || AREA || ' metros');
  ELSE
    DBMS_OUTPUT.put_line('Por favor, haga el favor de introducir valores positivos');
    DBMS_OUTPUT.put_line('Error con los datos introducidos');
  END IF;
END;

EXECUTE EJERCICIO_12(10,5);


CREATE OR REPLACE PROCEDURE EJERCICIO_13(NOTA1 NUMBER, NOTA2 NUMBER, NOTA3 NUMBER)
AS
  RESULTADO NUMBER;
  MEDIA NUMBER;
BEGIN
  IF (NOTA1 BETWEEN 0 AND 10) THEN
    IF (NOTA2 BETWEEN 0 AND 10) THEN
      IF (NOTA3 BETWEEN 0 AND 10) THEN
        RESULTADO := ((NOTA1 + NOTA2 + NOTA3) / 3);
        MEDIA := (ROUND(RESULTADO,2));
        DBMS_OUTPUT.put_line('Buenos días, procedamos a calcular la nota de BBDD: ');
        DBMS_OUTPUT.put_line('La nota redondeada será: ' || MEDIA );
        ELSE
          DBMS_OUTPUT.put_line('Error EN LA TERCERA NOTA, introduce valores entre 0 y 10');
        END IF;
      ELSE
        DBMS_OUTPUT.put_line('Error EN LA SEGUNDA NOTA, introduce valores entre 0 y 10');
      END IF;
    ELSE
      DBMS_OUTPUT.put_line('Error EN LA PRIMERA NOTA, introduce valores entre 0 y 10');
    END IF;
END;


