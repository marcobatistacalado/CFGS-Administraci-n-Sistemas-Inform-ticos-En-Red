CREATE OR REPLACE PROCEDURE EJER_09 (PPRECIO NUMBER,PPORCENTAJE NUMBER)
AS
RESULTADO NUMBER;
BEGIN
IF PPORCENTAJE NOT BETWEEN 0 AND 100 THEN
   DBMS_OUTPUT.PUT_LINE ('EL PORCENTAJE TIENE QUE ESTAR ENTRE 0 Y 100');
